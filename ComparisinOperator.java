package Operator;

public class ComparisinOperator {

	public static void main(String[] args) {
		int a = 100;
		int b = 200;
		System.out.println(a < b);// True
		System.out.println(a > b);// False
		System.out.println(a <= b);//True
		System.out.println(a >= b);//False
		System.out.println(a == b);//False
		System.out.println(a != b);//True

	}

}
