package Demo;

public class LogicalSwap {

	public static void main(String[] args) {
		// Swapping number
		int a = 10, b = 20;

		System.out.println("Before swapping  number " + a + " " + b);
		int t = a;
		a = b;
		b = t;
		System.out.println("Afore swapping  number " + a + " " + b);

	}

}
