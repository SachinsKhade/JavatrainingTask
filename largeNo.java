package LogicalProgram;

public class largeNo {

	public static void main(String[] args) {
		// Variable Declaration
		int firstNo = 10, SecondNo = 20, max;
		System.out.println("First Number is = " + firstNo);
		System.out.println("Second Number is = " + SecondNo);
		// Largest Among First and second number
		max = firstNo > SecondNo ? firstNo : SecondNo;
		// Printing The max Number

		System.out.println("Max Number is =" + max);
	}

}
